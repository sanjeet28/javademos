
public class charAndBooleans {

	public static void main(String[] args) {
		char myChar = 'D';
		System.out.println("myChar:" + myChar);
              int i = myChar;
              System.out.println(i);
              char x = 69;
              System.out.println("Char x:" + x);
              
              char mynewChar = '\u0044';
              System.out.println(mynewChar);
              
              mynewChar = '\u0049';
              System.out.println(mynewChar);
              
              boolean isTrue = true;
              System.out.println(isTrue);
	}

}
